<template>
  <div>
    <List
      :columns="cList.columns"
      :data="list.items"
      :total="list.total"
      :current="cList.cPage.current"
      @on-change="handlePageChange">
      <ListHeader>
        <ListOperations>
          <Button
            type="primary"
            @click="handlePost">
            新增
          </Button>
        </ListOperations>
      </ListHeader>
    </List>

    <Modal
      width="280"
      v-model="cDel.modal"
      title="请确认"
      @on-ok="handleDelOk">
      <p>确认删除？</p>
    </Modal>

    <Modal
      width="400"
      v-model="cForm.modal"
      :title="cForm.id ? '编辑' : '新增'">
      <Form
        ref="formValidate"
        :model="cForm.formValidate"
        :rules="cForm.ruleValidate"
        :label-width="80">
        <Form-item
          label="标题"
          prop="title">
          <Row>
            <Col span="20">
              <Input
                v-model="cForm.formValidate.title"
                placeholder="请输入标题" />
            </Col>
          </Row>
        </Form-item>
      </Form>
      <div slot="footer">
        <Button
          type="text"
          size="large"
          @click="cForm.modal = false">
          取消
        </Button>
        <Button
          type="primary"
          size="large"
          @click="handleFormOk">
          确定
        </Button>
      </div>
    </Modal>
  </div>
</template>

<script>
  import { mapState } from 'vuex'
  import List, { ListHeader, ListOperations } from '@/components/List'

  const module = 'videos'

  export default {
    components: {
      List,
      ListHeader,
      ListOperations
    },
    data () {
      return {
        cList: {
          columns: [
            {
              title: '标题',
              key: 'title'
            },
            {
              title: '操作',
              key: 'action',
              width: 150,
              render: (h, params) => {
                return h('ButtonGroup', [
                  h(
                    'Button',
                    {
                      props: {
                        type: 'ghost'
                      },
                      on: {
                        click: () => {
                          this.handlePut(params.row)
                        }
                      }
                    },
                    '编辑'
                  ),
                  h(
                    'Button',
                    {
                      props: {
                        type: 'ghost'
                      },
                      on: {
                        click: () => {
                          this.handleDel(params.row.id)
                        }
                      }
                    },
                    '删除'
                  )
                ])
              }
            }
          ],
          cPage: {
            current: 1
          }
        },
        cDel: {
          id: 0,
          modal: false
        },
        cForm: {
          id: 0,
          modal: false,
          formValidate: {},
          ruleValidate: {
            title: [
              {
                required: true,
                message: '标题不能为空'
              },
              {
                max: 100,
                message: '标题不能多于 100 个字'
              }
            ]
          }
        }
      }
    },
    computed: mapState({
      list: state => state[module].list
    }),
    methods: {
      getList (current = 1) {
        this.cList.cPage.current = current

        return this.$store.dispatch(`${module}/getList`, {
          query: {
            offset: (current - 1) * this.consts.PAGE_SIZE,
            limit: this.consts.PAGE_SIZE
          }
        })
      },
      handlePageChange (current) {
        this.getList(current)
      },
      handlePost () {
        this.cForm.modal = true
        this.cForm.id = 0
        this.resetFields()
      },
      handlePut (detail) {
        this.cForm.id = detail.id
        this.$set(this.cForm, 'formValidate', { title: detail.title })
        this.cForm.modal = true
      },
      handleDel (id) {
        this.cDel.id = id
        this.cDel.modal = true
      },
      async handleDelOk () {
        await this.$store.dispatch(`${module}/del`, { id: this.cDel.id })
        this.$Message.success('删除成功！')
        this.getList()
      },
      handleFormOk () {
        this.$refs.formValidate.validate(async valid => {
          if (valid) {
            await this.$store.dispatch(
              this.cForm.id ? `${module}/put` : `${module}/post`,
              {
                id: this.cForm.id || '0',
                body: { title: this.cForm.formValidate.title }
              }
            )

            this.cForm.modal = false
            this.$Message.success((this.cForm.id ? '编辑' : '新增') + '成功！')
            !this.cForm.id && this.resetFields()
            this.getList()
          }
        })
      },
      resetFields () {
        this.$refs.formValidate.resetFields()
        this.$set(this.cForm, 'formValidate', {})
      }
    },
    created () {
      console.log(33)
      this.getList()
    }
  }
</script>
